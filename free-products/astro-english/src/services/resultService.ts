export async function getAstroResult(
  zodiac: ZodiacSign,
  _answers: Record<string, string>
): Promise<QuizResult> {
  await new Promise(resolve => setTimeout(resolve, 2000));
  
  const baseData = DATA.zodiac_alter_ego_data.find(d => d.sign === zodiac);
  const randomDescs = DATA.zodiac_descriptions_random.find(d => d.sign === zodiac)?.descriptions || [];
  const roast = randomDescs[Math.floor(Math.random() * randomDescs.length)];

  if (!baseData) throw new Error("Sign not found");

  return {
    persona: baseData.alter_ego,
    roast: roast,
    advice: baseData.learning_plan.map(p => `- ${p}`).join("\n"),
    imageUrl: `https://picsum.photos/seed/${zodiac}-${Math.random()}/800/800`
  };
}
